private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {                                          
        try{
            JFileChooser file = new JFileChooser(); // Create a new instance ofJFileChooser class
            file.setCurrentDirectory(new File(System.getProperty("user.home")));// Set the current directory, initial directory where the dialog looks for files. This line sets the current directory to user’s home directory.
            FileNameExtensionFilter filter = new FileNameExtensionFilter(".Images", "jpg", "gif", "png");
            file.addChoosableFileFilter(filter); // Adds filter to the list of user choosable file filters.
            int result = file.showSaveDialog(null);
            //Check if the user selects a file or not.If the user selects a file, the variable result will hold a value of JFileChooser.APPROVE_OPTION.
            if(result == JFileChooser.APPROVE_OPTION){
                File selectedFile = file.getSelectedFile(); //Pick up the selected file:
                pathA = selectedFile.getAbsolutePath();  //Take absolute path  of the file
                uploadImage.setIcon(ResizeImage(pathA)); //Set icon to uploadImage lable

            }
            else if (result == JFileChooser.CANCEL_OPTION){
                System.out.println("No File Select");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }                                          

